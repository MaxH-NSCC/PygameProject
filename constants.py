# ENEMY
class Enemy:
    def __init__(self, x, y, width, height, image, type, speed, health, attack, damage, score):
        self.rect = pygame.Rect(x, y, width, height)
        self.image = pygame.image.load(image)
        self.type = type
        self.speed = speed
        self.last_print_time = 0
        self.last_attack_time = 0
        self.health = health
        self.attack = attack
        self.damage = 0
        self.score = score
    
    def collides_with(self, other_rect):
        return self.rect.colliderect(other_rect)
    
    def move(self, player, enemies, room, attack, attack_x):
        # Calculate the direction vector from the enemy to the player
        dx = player.x - self.rect.x
        dy = player.y - self.rect.y
        # Make sure distance is direction and the enemy does not move faster the further the player is from it
        # This makes updates accurate and smooth, without this the game will crash
        distance = max(1, (dx ** 2 + dy ** 2) ** 0.5)
        dx /= distance
        dy /= distance

        # Update the enemy's position
        new_rect = self.rect.move(dx * self.speed, dy * self.speed)

        # Taking damage
        if attack_x == 0:
            pass
        else:
            if attack.colliderect(self.rect):
                    # Cooldown for enemy taking damage
                current_time = pygame.time.get_ticks()
                if current_time - self.last_attack_time >= 500:
                    self.health -= 1
                    pygame.mixer.Sound(ATTACK).play()
                    self.last_attack_time = current_time
        
        # Check for collisions with walls
        if not f.collides_walls(new_rect, room):
            # Check for collisions with other enemies
            for other_enemy in enemies:
                if other_enemy != self and self.collides_with(other_enemy.rect):
                    # Calculate the direction
                    enemy_dx = other_enemy.rect.x - self.rect.x
                    enemy_dy = other_enemy.rect.y - self.rect.y

                    # Normalize the direction
                    enemy_distance = max(1, (enemy_dx ** 2 + enemy_dy ** 2) ** 0.5)
                    enemy_dx /= enemy_distance
                    enemy_dy /= enemy_distance

                    # Check if the enemy is moving towards the other enemy
                    if dx * enemy_dx + dy * enemy_dy > 0:
                        return

            # Check for collisions with the player
            # Attacking

            if player.colliderect(new_rect):
                # Calculate cooldown for attack
                current_time = pygame.time.get_ticks()
                if current_time - self.last_print_time >= 1000:
                    self.damage = 1 # 1 means it is attacking player
                    self.last_print_time = current_time
            
            if not player.colliderect(new_rect):
                self.rect = new_rect

